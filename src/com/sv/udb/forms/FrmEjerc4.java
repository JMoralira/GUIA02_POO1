     private void radis(int radi){
        switch(radi) {
            case 1:
                this.rb1.setSelected(true);
                break;
            case 2:
                this.rb2.setSelected(true);
                break;
            case 3:
                this.rb3.setSelected(true);
                break;
            case 4:
                this.rb4.setSelected(true);
                break;
            default:
                this.rb1.setSelected(false);
                this.rb2.setSelected(false);
                this.rb3.setSelected(false);
                this.rb4.setSelected(false);
                break;
        }
    }
    private void Intentos(String numero) {
        if (codigo.length() == 0){
            radis(0);
            //Sino se a intentado radis es igual a 0
        }
        this.codigo = codigo.concat(numero); //cadena de digitos ingresados
        if(this.codigo.length() == 4){ //cuando hayan 4 digitos
            if (Integer.parseInt(this.codigo) == this.Ejeobje.getPin()) {// comparar con el intento con el PIN
                JOptionPane.showMessageDialog(this, "Â¡PIN CORRECTO!");
                
                //se reinicia todo
                radis(0);
                this.codigo = "";
                this.Ejeobje = new Ejercicio4();                 
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Su PIN es incorrecto");
                this.codigo = "";//se inican de nuevo los radiobutton
                cont ++; //suma numero de intentos
               
                  if( cont == 1) {
                    lbl.setText("Usted ha fallado su primer intento");
                    }
                else if( cont == 2) {
                    lbl.setText("Usted ha fallado su segundo intento");
                    }
                 else if( cont == 3) {
                     lbl.setText("Usted ha fallado su tercer intento");
                   JOptionPane.showMessageDialog(this, "Usted ha fallado su tercer intento, ha superado el numero de intentos");
                    this.dispose();
                }
            }
        }
        radis(this.codigo.length());
    
    
    }
    
    
    
    
    
    
